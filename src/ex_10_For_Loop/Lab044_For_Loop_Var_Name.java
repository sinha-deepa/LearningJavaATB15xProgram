package ex_10_For_Loop;

public class Lab044_For_Loop_Var_Name {
    public static void main(String[] args) {
        System.out.println("somya");
        for (int somya = 0; somya < 10; somya++) {
            System.out.println(somya);
        }
        System.out.println("_1");
        for (int _1 = 0; _1 < 10; _1++) {
            System.out.println(_1);
        }
    }
}
